    private void atualizarTabela(){
        try{
            List<Clientes> listaClientes = new ClienteDAO().selecionarTodos();
            TableModel tableModelFuncionario = TableModelCreator.createTableModel(Clientes.class, listaClientes, null);
            tblCliente.setModel(tableModelFuncionario);
        }catch (Exception ex){
            
        }
    }
    private void preencher(){
        Clientes cliente = new ClienteDAO().selecionarPorCodigo(idSelecionado);
        txtNome.setText(cliente.getNome());
        txtDataNascimento.setText(cliente.getDataNascimento());
        txtTelefone.setText(cliente.getTelefone());
        txtCpf.setText(cliente.getCpf());
    }
    private void limpar(){
        txtDataNascimento.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
        txtCpf.setText("");
    }

