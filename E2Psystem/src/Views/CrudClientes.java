    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked
        JTable tabela = (JTable) evt.getSource();
        int linha = tabela.rowAtPoint(evt.getPoint());
        for (int i = 0; i < tabela.getModel().getColumnCount(); i++) {
            String nomeColuna = tabela.getModel().getColumnName(i);
            if (nomeColuna.trim().equals("Id")) {
                idSelecionado = Integer.parseInt(tabela.getValueAt(linha, i) + "");
                break;
            }
        }
        preencher();
        btnAtualizar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_tblClienteMouseClicked

    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
       if (objetoCliente == null) {
            objetoCliente = new Entities.Clientes();
        }
        objetoCliente.setNome(txtNome.getText());
        objetoCliente.setDataNascimento(txtDataNascimento.getText());
        objetoCliente.setTelefone(txtTelefone.getText());
        objetoCliente.setCpf(txtCpf.getText().replace(".","").replace("-",""));
        
        if(objetoCliente == null){
            new ClienteDAO().inserir(objetoCliente);
        }
        else
            new ClienteDAO().editar(objetoCliente);
        JOptionPane.showMessageDialog(this, "Salvo com sucesso","Atenção",JOptionPane.INFORMATION_MESSAGE);
        atualizarTabela();
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
       Clientes cliente = new ClienteDAO().selecionarPorCodigo(idSelecionado);
       cliente.setNome(txtNome.getText());
       cliente.setTelefone(txtTelefone.getText());
       cliente.setDataNascimento(txtDataNascimento.getText());
       cliente.setCpf(txtCpf.getText());
       new ClienteDAO().editar(cliente);
       atualizarTabela();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Deseja realmente excluir?", "Atenção",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            ClienteDAO clienteDAO = new ClienteDAO();
            Clientes cliente = clienteDAO.selecionarPorCodigo(idSelecionado);
            clienteDAO.excluir(cliente.getId());
            JOptionPane.showMessageDialog(this, "Excluído com sucesso", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            limpar();
            atualizarTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

